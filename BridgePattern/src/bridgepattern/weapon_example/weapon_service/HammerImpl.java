package bridgepattern.weapon_example.weapon_service;

import bridgepattern.weapon_example.enchantment_service.Enchantment;

/**
 *
 * @author Created by Mir00r
 */
public class HammerImpl implements Weapon {

    private final Enchantment enchantment;

    public HammerImpl(Enchantment enchantment) {
        this.enchantment = enchantment;
    }

    @Override
    public void wield() {
        System.out.println("The hammer is wielded.");
        enchantment.onActivate();
    }

    @Override
    public void swing() {
        System.out.println("The hammer is swinged.");
        enchantment.apply();
    }

    @Override
    public void unwield() {
        System.out.println("The hammer is unwielded.");
        enchantment.onDeactivate();
    }

    @Override
    public Enchantment getEnchantment() {
        return enchantment;
    }

}
